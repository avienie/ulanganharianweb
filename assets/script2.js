let hari = "Senin";
switch (hari) {
    case "Minggu":
        console.log("Hari Minggu");
        break;
    case "Senin":
        console.log("Saya pilih hari Sabtu");
        break;
    case "Selasa":
        console.log("Hari Selasa");
        break;
    case "Rabu":
        console.log("Hari Rabu");
        break;
    case "Kamis":
        console.log("Hari Kamis");
        break;
    case "Jumat":
        console.log("Hari Jumat");
        break;
    case "Sabtu":
        console.log("Hari ini hari Sabtu");
        break;
    default:
        console.log("Hari salah");
}